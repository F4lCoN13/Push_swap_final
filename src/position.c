#include "headerpush_swap.h"

///////////////////////////////////////////////////////////////////////////////
//////////////FR: Donne la position de la node dans la stack///////////////////
//////////////EN: Give the position of the node in the stack///////////////////
///////////////////////////////////////////////////////////////////////////////
static void	get_position(t_stack **stack)
{
	t_stack	*tmp;
	int		i;

	tmp = *stack;
	i = 0;
	while (tmp)
	{
		tmp->pos = i;
		tmp = tmp->next;
		i++;
	}
}

///////////////////////////////////////////////////////////////////////////////
////////////////FR: trouve l'index le plus bas dans la stack///////////////////
///////////////////EN: find the lowest index in the stack//////////////////////
///////////////////////////////////////////////////////////////////////////////
int	get_lowest_index_position(t_stack **stack)
{
	t_stack	*tmp;
	int		lowest_index;
	int		lowest_pos;

	tmp = *stack;
	lowest_index = INT_MAX;
	get_position(stack);
	lowest_pos = tmp->pos;
	while (tmp)
	{
		if (tmp->index < lowest_index)
		{
			lowest_index = tmp->index;
			lowest_pos = tmp->pos;
		}
		tmp = tmp->next;
	}
	return (lowest_pos);
}

///////////////////////////////////////////////////////////////////////////////
///////FR: Trouve la meilleur position pour target pos et son index////////////
/////////EN: Find the best position for target pos and its index///////////////
///////////////////////////////////////////////////////////////////////////////
static int	get_target(t_stack **a, int b_idx, int target_idx, int target_pos)
{
	t_stack	*tmp_a;

	tmp_a = *a;
	while (tmp_a)
	{
		if (tmp_a->index > b_idx && tmp_a->index < target_idx)
		{
			target_idx = tmp_a->index;
			target_pos = tmp_a->pos;
		}
		tmp_a = tmp_a->next;
	}
	if (target_idx != INT_MAX)
		return (target_pos);
	tmp_a = *a;
	while (tmp_a)
	{
		if (tmp_a->index < target_idx)
		{
			target_idx = tmp_a->index;
			target_pos = tmp_a->pos;
		}
		tmp_a = tmp_a->next;
	}
	return (target_pos);
}

///////////////////////////////////////////////////////////////////////////////
///FR: Assigne la postion et le nombre de mouvement ideal
///////////////////////////////////////////////////////////////////////////////
void	get_target_position(t_stack **a, t_stack **b)
{
	t_stack	*tmp_b;
	int		target_pos;

	tmp_b = *b;
	get_position(a);
	get_position(b);
	target_pos = 0;
	while (tmp_b)
	{
		target_pos = get_target(a, tmp_b->index, INT_MAX, target_pos);
		tmp_b->target_pos = target_pos;
		tmp_b = tmp_b->next;
	}
}
